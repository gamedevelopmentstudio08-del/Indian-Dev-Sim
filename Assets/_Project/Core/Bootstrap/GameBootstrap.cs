        Vector3 roadRight = new Vector3(-flatForward.z, 0f, flatForward.x);
        Vector3 leftColumnPosition = new Vector3(bridgePoint.x, groundHeight + supportHeight * 0.5f, bridgePoint.z) - roadRight * 4.6f;
